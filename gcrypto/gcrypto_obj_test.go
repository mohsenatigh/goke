package gcrypto

import (
	"bytes"
	"testing"
)

func TestGCryptoPRFPlusFunction(t *testing.T) {

	body := []byte{0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15}
	key := []byte{0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15}
	res := []byte{0xcf, 0x1f, 0x83, 0x13, 0xb2, 0x70, 0xdb, 0x58, 0x28, 0x57, 0x7e, 0x9f, 0x8d, 0x36, 0xc2, 0xc2}
	hmac := createCryptoHMAC(IANA_PRF_HMAC_MD5, key)

	prfPlus := CreateCryptoObject().CalculatePRFPlus(body, hmac)
	if !bytes.Equal(res[:], prfPlus[:len(res)]) {
		t.FailNow()
	}
}
